def get_price(call):
    price = 0.36
    for time in range(call['start']+60, call['end']+1, 60):
        hour = datetime.fromtimestamp(time).hour
        if hour >= 6 and hour <= 22:
            price = price + 0.09
    return price


def classify_by_phone_number(records):
    prices = {}
    result = []
    for call in records:
        source = call['source']
        price = get_price(call)
        if source in prices:
            prices[source] += price
        else:
            prices[source] = price

    for source in prices:
        result.append({'source': source, 'total': round(prices[source], 2)})

    result.sort(reverse=True, key=lambda i: i['total'])

    return result
